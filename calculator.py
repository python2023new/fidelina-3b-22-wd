def add(arg1, arg2):
    return int(arg1) + int(arg2)

def sub(arg1, arg2):
    return int(arg1) - int(arg2)

def mult(arg1, arg2):
    return int(arg1) * int(arg2)

def div(arg1, arg2):
    return int(arg1) / int(arg2)